def resolve_groups(requested, force_cpu=False, force_gpu=False):
    """根據 --with / --force-cpu / --force-gpu 組出最終 groups 清單。

    衝突規則：
    - ai-runtime-gpu 與 ai-runtime-cpu 不得同時啟用
    - --force-cpu 會拒絕 ai-runtime-gpu（並 warn）
    - --force-gpu 會拒絕 ai-runtime-cpu（並 warn）
    """
    groups = []
    seen = set()
    for g in requested or []:
        if g not in _PIP_OPTIONAL_GROUPS:
            print(f"  [ERROR] 未知 group: '{g}'（可用：{', '.join(_PIP_OPTIONAL_GROUPS)}）")
            sys.exit(2)
        if g in seen:
            continue
        seen.add(g)
        groups.append(g)

    if "ai-runtime-gpu" in seen and "ai-runtime-cpu" in seen:
        print("  [ERROR] ai-runtime-gpu 與 ai-runtime-cpu 不可同時啟用")
        sys.exit(2)

    if force_cpu and "ai-runtime-gpu" in seen:
        print("  [WARN] --force-cpu 與 ai-runtime-gpu 衝突，移除 ai-runtime-gpu")
        groups = [g for g in groups if g != "ai-runtime-gpu"]
    if force_gpu and "ai-runtime-cpu" in seen:
        print("  [WARN] --force-gpu 與 ai-runtime-cpu 衝突，移除 ai-runtime-cpu")
        groups = [g for g in groups if g != "ai-runtime-cpu"]

    return groups


def generate_environment_yml(groups):
    """產生 environment.yml 內容。groups 是已解析的 optional group 清單。"""
    groups_label = ", ".join(groups) if groups else "core-only"
    header = _ENV_HEADER.format(groups=groups_label)
    body = _PIP_CORE
    for g in groups:
        body += _PIP_OPTIONAL_GROUPS[g]
    return header + body


def write_environment_yml(groups, force=False):
    """寫入 environment.yml。groups 不同時會覆寫。"""
    env_path = ROOT / "environment.yml"
    new_content = generate_environment_yml(groups)
    groups_label = ", ".join(groups) if groups else "core-only"

    if env_path.exists() and not force:
        existing = env_path.read_text(encoding="utf-8")
        if f"groups: {groups_label}" in existing:
            print(f"  [SKIP] environment.yml 已是 groups: {groups_label}")
            return False
        print(f"  [UPDATE] environment.yml → groups: {groups_label}")
    else:
        print(f"  [CREATE] environment.yml (groups: {groups_label})")

    env_path.write_text(new_content, encoding="utf-8")
    return True



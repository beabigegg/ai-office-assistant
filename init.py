def main():
    parser = argparse.ArgumentParser(
        description="AI Office Assistant 初始化",
        formatter_class=argparse.RawDescriptionHelpFormatter,
        epilog="可用 optional groups：" + ", ".join(_PIP_OPTIONAL_GROUPS.keys()),
    )
    parser.add_argument("--project", "-p", help="同時建立一個新專案")
    parser.add_argument(
        "--with", dest="with_groups", action="append", default=[], metavar="GROUP",
        help="啟用 optional group（可重複使用，如 --with ocr --with ml）",
    )
    hw_group = parser.add_mutually_exclusive_group()
    hw_group.add_argument("--force-cpu", action="store_true",
                          help="強制 CPU 模式（拒絕 ai-runtime-gpu）")
    hw_group.add_argument("--force-gpu", action="store_true",
                          help="強制 GPU 模式（拒絕 ai-runtime-cpu）")
    args = parser.parse_args()

    print("=" * 50)
    print("AI Office Assistant — 初始化")
    print("=" * 50)

    # Step 1: 硬體偵測
    print("\n[1/7] 硬體偵測")
    os_info = detect_os_info()
    gpu_info = detect_gpu()
    print_hw_summary(os_info, gpu_info)
    if args.force_cpu:
        print("  (--force-cpu 已指定)")
    elif args.force_gpu:
        print("  (--force-gpu 已指定)")

    # Step 2: 解析 groups + 產生 environment.yml
    print("\n[2/7] 產生 environment.yml")
    groups = resolve_groups(args.with_groups, args.force_cpu, args.force_gpu)
    if groups:
        print(f"  Optional groups: {', '.join(groups)}")
    else:
        print("  Optional groups: (none — core only)")
        if gpu_info["gpu_available"] and not args.force_cpu:
            print("  [TIP] 偵測到 GPU；如需 PyTorch，請加 --with ai-runtime-gpu")
        elif not gpu_info["gpu_available"]:
            print("  [TIP] 未偵測到 GPU；如需 PyTorch CPU 版，請加 --with ai-runtime-cpu")
    env_changed = write_environment_yml(groups)

    # Step 3: Conda 環境檢查
    print("\n[3/7] Conda 環境檢查")
    check_conda_env()
    if env_changed:
        print("  [INFO] environment.yml 已更新，請執行：")
        print("         conda env update -f environment.yml --prune")

    # Step 4: 建立目錄
    print("\n[4/7] 建立目錄結構")
    created_dirs = create_dirs()
    if created_dirs:
        for d in created_dirs:
            print(f"  [OK] {d}/")
    else:
        print("  [SKIP] 所有目錄已存在")

    # Step 5: 建立模板檔案
    print("\n[5/7] 建立模板檔案")
    created_files, skipped_files = create_templates()
    if created_files:
        for f in created_files:
            print(f"  [OK] {f}")
    if skipped_files:
        print(f"  [SKIP] {len(skipped_files)} 個檔案已存在")

    # Step 6: 初始化 KB 索引
    print("\n[6/7] 初始化知識庫索引")
    init_kb_index()

    # Step 7: 環境變數檢查
    print("\n[7/7] 環境變數檢查")
    check_env_file()

    # 可選：建立新專案
    if args.project:
        print(f"\n[+] 建立專案: {args.project}")
        create_project(args.project)

    # 完成摘要
    print("\n" + "=" * 50)
    groups_label = ", ".join(groups) if groups else "core only"
    print(f"初始化完成！(groups: {groups_label})")
    print()
    print("下一步：")
    if env_changed:
        print("  0. conda env update -f environment.yml --prune")
    print("  1. 編輯 .env 填入實際金鑰")
    print("  2. 確認 .mcp.json 路徑正確")
    print("  3. 建立新專案：python init.py --project <name>")
    print()
    print("切換或新增套件群組：")
    print("  python init.py --with <group> [--with <group>...]")
    print(f"  可用 groups：{', '.join(_PIP_OPTIONAL_GROUPS.keys())}")
    print("=" * 50)


if __name__ == "__main__":
    main()

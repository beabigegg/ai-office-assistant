def detect_os_info():
    """偵測作業系統資訊，回傳 dict。"""
    return {
        "system": platform.system(),
        "release": platform.release(),
        "machine": platform.machine(),
        "is_windows": sys.platform == "win32",
        "is_linux": sys.platform.startswith("linux"),
        "is_macos": sys.platform == "darwin",
    }


def detect_gpu():
    """偵測 NVIDIA GPU，回傳 dict。無 GPU 時 gpu_available=False。"""
    result = {
        "gpu_available": False,
        "gpu_name": None,
        "vram_mb": None,
        "cuda_driver": None,
    }
    try:
        output = subprocess.check_output(
            ["nvidia-smi", "--query-gpu=name,memory.total,driver_version",
             "--format=csv,noheader,nounits"],
            stderr=subprocess.DEVNULL,
            timeout=10,
        ).decode("utf-8", errors="replace").strip()
        if output:
            parts = [p.strip() for p in output.split("\n")[0].split(",")]
            if len(parts) >= 3:
                result["gpu_available"] = True
                result["gpu_name"] = parts[0]
                result["vram_mb"] = int(float(parts[1]))
                result["cuda_driver"] = parts[2]
    except (FileNotFoundError, subprocess.TimeoutExpired,
            subprocess.CalledProcessError, ValueError, IndexError):
        pass
    return result


def print_hw_summary(os_info, gpu_info):
    """印出硬體偵測摘要。"""
    print(f"  OS      : {os_info['system']} {os_info['release']} ({os_info['machine']})")
    if gpu_info["gpu_available"]:
        print(f"  GPU     : {gpu_info['gpu_name']} ({gpu_info['vram_mb']} MB VRAM)")
        print(f"  CUDA drv: {gpu_info['cuda_driver']}")
    else:
        print("  GPU     : 未偵測到 NVIDIA GPU")


